%% Test findStorageColumn

import org.opensim.modeling.Storage
assert(findStorageColumn(Storage("example.sto"), ...
    "hip_adduction_l") == [-0.475484233516851, -0.7097313776326962, ...
    -0.8224722507878384])
assert(findStorageColumn(Storage("example.sto"), 0) == ...
    [-0.475484233516851, -0.7097313776326962, -0.8224722507878384])
